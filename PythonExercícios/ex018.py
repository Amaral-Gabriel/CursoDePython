import math
# Coleta o ângulo
ang = float(input("Digite um ângulo:"))

print(f"A ângulo de {ang} tem o Seno de {math.sin(math.radians(ang)):.2f}")
print(f"A ângulo de {ang} tem o Cosseno de {math.cos(math.radians(ang)):.2f}")
print(f"A ângulo de {ang} tem a Tangente de {math.tan(math.radians(ang)):.2f}")
